public class VideoImage {
    private int id;
    private int video;
    private String image;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public int getVideo() {
        return video;
    }
    public void setVideo(int video) {
        this.video = video;
    }

    public String getImage() {
        return image;
    }
    public void setImage(String image) {
        this.image = image;
    }
}
